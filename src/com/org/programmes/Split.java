package com.org.programmes;

public class Split {

	public static void main(String[] args) {
	
		int a = 11;
		
		for (int i = 2; i < a / 2; i++) {
			
			int b = a % i;
			
			if (b == 0) {
				System.out.println("It is a prime number");				
			}
			else
				System.out.println("It is a prime number");
			
			
		}
		
	}
}
